
let f1 = 5; // Declare a variable f1 and assign the value 5
let f2 = 2; // Declare a variable f2 and assign the value 2

console.log(f1 - f2); // Output the result of subtracting f2 from f1
// Explanation: Subtract the value of f2 (2) from the value of f1 (5).
// The result is the difference between f1 and f2, which is 3.
// The result will be printed to the console.

// Multiplication Example

let g1 = 5; // Declare a variable g1 and assign the value 5
let g2 = 2; // Declare a variable g2 and assign the value 2

console.log(g1 * g2); // Output the result of multiplying g1 by g2
// Explanation: Multiply the value of g1 (5) by the value of g2 (2).
// The result is the product of g1 and g2, which is 10.
// The result will be printed to the console.
